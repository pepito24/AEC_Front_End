var nombre;

nombre = Number(prompt("Écrire un nombre"));

if (nombre === 1){
    document.write("Janvier");
}

else if (nombre === 2){
    document.write("Février");
}

else if (nombre === 3){
    document.write("Mars");
}

else if (nombre === 4){
    document.write("Avril");
}

else if (nombre === 5){
    document.write("Mai");
}

else if (nombre === 6){
    document.write("Juin");
}

else if (nombre === 7){
    document.write("Juillet");
}

else if (nombre === 8){
    document.write("Aout");
}

else if (nombre === 9){
    document.write("Septembre");
}

else if (nombre === 10){
    document.write("octobre");
}

else if (nombre === 11){
    document.write("Novembre");
}

else if (nombre === 12){
    document.write("Décembre");
}

else{
    document.write("Mois invalide");
}